// Output even numbers in the loop
for (i = 1; i <= 10; i++) {
  if (i % 2 === 0) {
    console.log(i);
  }
}
// Output prime numbers
for (n = 1; n <= 17; n++) {
  if (n % 2 > 0 && n > 1) {
    console.log(n);
  }
}
